#Library import portion
from sklearn.linear_model import LinearRegression
from sklearn.metrics import mean_absolute_error, mean_squared_error, root_mean_squared_error
import pandas as pd

#Data loded portion
data = {
    "Houre" : [1, 2, 3, 4, 5],
    "Result" : [20, 40, 60, 80, 100]
}

df = pd.DataFrame(data)

#Model portion
X = df[["Houre"]]   #input
y = df[["Result"]]  #Target column OR actully result of student

model = LinearRegression()
model.fit(X,y)

predicted_score = model.predict(X)    #machine prediction of student result

#evaluat(compare between Actul result and Machine result)
mae = mean_absolute_error(y, predicted_score)
mse = mean_squared_error(y, predicted_score)
rmse = root_mean_squared_error(y, predicted_score)

#show result
print("Mean Absolute Error (MAE):", mae)
print("Mean Squared Error (MSE):", mse)
print("Root Mean Squared Error (RMSE):", rmse)